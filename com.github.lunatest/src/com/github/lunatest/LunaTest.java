package com.github.lunatest;

import java.io.IOException;
import java.net.URL;

import org.eclipse.core.runtime.FileLocator;
import org.eclipse.core.runtime.Path;
import org.osgi.framework.BundleActivator;
import org.osgi.framework.BundleContext;

public class LunaTest implements BundleActivator {

	private static BundleContext context;
	private static String luaFolder = "lib";

	static BundleContext getContext() {
		return context;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see
	 * org.osgi.framework.BundleActivator#start(org.osgi.framework.BundleContext
	 * )
	 */
	public void start(BundleContext bundleContext) throws Exception {
		LunaTest.context = bundleContext;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see
	 * org.osgi.framework.BundleActivator#stop(org.osgi.framework.BundleContext)
	 */
	public void stop(BundleContext bundleContext) throws Exception {
		LunaTest.context = null;
	}

	public static Path getPath() throws IOException {
		URL ressource = getContext().getBundle().getResource(luaFolder);
		return new Path(FileLocator.toFileURL(ressource).getPath());
	}
}
